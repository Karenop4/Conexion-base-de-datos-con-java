    private void aLbtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aLbtnAgregarActionPerformed
        if(aLCodigo.getText().equals("") || aLFecha.getText().equals("") || aLPrecio.getText().equals("") || aLTitulo.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Primero llene todos los campos");
        }else{
            int codigoGenero = gC.devolverCodigoGenero((String)aLListaGenero.getSelectedItem());
            boolean libroCreado = lC.agregarLibro(Integer.parseInt(aLCodigo.getText()), aLTitulo.getText(), aLFecha.getText(), Double.parseDouble(aLPrecio.getText()), codigoGenero);
            if(libroCreado){
                JOptionPane.showMessageDialog(rootPane, "Libro Creado");
                elementosAgregarLibro();
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Codigo libro ya existe");
                aLCodigo.setText("");
            }
        }
        
            
    }//GEN-LAST:event_aLbtnAgregarActionPerformed

    private void mLbtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mLbtnBuscarActionPerformed
        ResultSet libro = null;
        try {
            int codigo = Integer.parseInt(mLCodigo.getText());
            libro = lC.buscarLibro(codigo);

            if (libro != null && libro.next()) {
                habilitarElementosModificarLibro(true);
                mLTitulo.setText(libro.getString("titulo"));
                mLFecha.setText(libro.getString("fechaPublicacion"));
                mLPrecio.setText(String.valueOf(libro.getDouble("precio")));
                mLTitulo.setText(libro.getString("titulo"));
                seleccionarGeneroEnComboBox(libro.getString("genero"));
            } else {
                JOptionPane.showMessageDialog(rootPane, "Libro no encontrado", "Búsqueda", JOptionPane.INFORMATION_MESSAGE);
                habilitarElementosModificarLibro(false);
                mLCodigo.setText("");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Por favor, ingrese un código válido", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(Vista.class.getName()).log(Level.SEVERE, "Error al procesar datos del libro", ex);
            JOptionPane.showMessageDialog(rootPane, "Error al procesar datos del libro", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_mLbtnBuscarActionPerformed

