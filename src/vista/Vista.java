    private void eLEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eLEliminarActionPerformed
        if(eLCodigo.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Primero busque un libro");
        }else{
            boolean eliminado = lC.eliminarLibro(Integer.parseInt(eLCodigo.getText()));
            if(eliminado){
                JOptionPane.showMessageDialog(rootPane, "Libro eliminado");
                elementosEliminar();
            }
            else
                JOptionPane.showMessageDialog(rootPane,"No se pudo eliminar el libro");
        }
    }//GEN-LAST:event_eLEliminarActionPerformed

    private void lLbtnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lLbtnListarActionPerformed
        ResultSet libro = lC.listarLibros();
        DefaultTableModel model = (DefaultTableModel) tabla.getModel();

        model.setRowCount(0); 
        try {
            while(libro.next()){
                String codigoLibro = String.valueOf(libro.getInt("codigoLibro"));
                String titulo = libro.getString("titulo");
                String fechaPublicacion = libro.getString("fechaPublicacion");
                String precio = String.valueOf(libro.getDouble("precio"));
                String genero = libro.getString("descripcion");

                String[] datos = {codigoLibro, titulo, fechaPublicacion, precio, genero};
                model.addRow(datos);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Vista.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lLbtnListarActionPerformed

