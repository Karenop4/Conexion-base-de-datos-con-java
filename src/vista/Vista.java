    private void elementosEliminar(){
        eLCodigo.setText("");
        DefaultTableModel model = (DefaultTableModel) tabla.getModel();
        model.setRowCount(0);
    }
    
    private void seleccionarGeneroEnComboBox(String genero) {
        for (int i = 0; i < mLListaGenero.getItemCount(); i++) {
            if (mLListaGenero.getItemAt(i).equals(genero)) {
                mLListaGenero.setSelectedIndex(i);
                break;
            }
        }
    }
    
    private void cargarGeneros(){
        ResultSet generos = gC.listarGeneros();
        DefaultComboBoxModel<String> model = (DefaultComboBoxModel<String>) aLListaGenero.getModel();
        DefaultComboBoxModel<String> model2 = (DefaultComboBoxModel<String>) mLListaGenero.getModel();
        
        try {
            while(generos.next()){
                model.addElement(generos.getString("descripcion"));
                model2.addElement(generos.getString("descripcion"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Vista.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void habilitarElementosModificarLibro(boolean habilitado){
        mLFecha.setEditable(habilitado);
        mLListaGenero.setEnabled(habilitado);
        mLPrecio.setEditable(habilitado);
        mLTitulo.setEditable(habilitado);
    }
    
    private void elementosAgregarLibro(){
        aLCodigo.setText("");
        aLFecha.setText("");
        aLListaGenero.setSelectedIndex(0);
        aLPrecio.setText("");
        aLTitulo.setText("");
    }
    
    private void elementosModificarLibro(){
        mLCodigo.setText("");
        mLFecha.setText("");
        mLListaGenero.setSelectedIndex(0);
        mLPrecio.setText("");
        mLTitulo.setText("");
    }
    
    private void mostrarDatosLibro(ResultSet libro) {
        DefaultTableModel model = (DefaultTableModel) tabla.getModel();
        
        model.setRowCount(0);
        try {
            String[] datos = {String.valueOf(libro.getInt("codigoLibro")), libro.getString("titulo"), libro.getString("fechaPublicacion"), String.valueOf(libro.getDouble("precio")), libro.getString("genero")};
            model.addRow(datos);
        } catch (SQLException ex) {
            Logger.getLogger(Vista.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
